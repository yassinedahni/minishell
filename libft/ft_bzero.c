#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*p;
	size_t			i;
	int				c;

	i = 0;
	p = (unsigned char *)s;
	c = 0;
	while (i < n)
	{
		p[i] = c;
		i++;
	}
}
